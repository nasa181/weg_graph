function call_graph1(url){
	
	return new Promise(function (resolve,reject){
		graph.setVersion("2.8");
		graph.setAccessToken(stringAccessToken);
		graph.batch([
		{
			method: "GET",
			relative_url: url
		}],
			function(err,mydata1){
				var text = '';
				if(mydata1){
					var jsonData = decode(mydata1[0].body).posts.data;
					// z++;
					// console.log("z value : " + z);
					for (var j in jsonData){
			        	text += jsonData[j].message + "</br></br></br></br></br>";
			        }
			        // resolve(text);
					resolve(jsonData);
				}
				else reject(err);
			}
		);
	})
		
	
	
}

